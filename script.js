const menuButton = document.querySelector("[data-menu-toggle]");
const navLinks = document.querySelector("[data-nav-links]");
const languageButton = document.querySelector("[data-language-toggle]");

function getRequestedLanguage() {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get("lang");
  if (fromUrl === "zh" || fromUrl === "en") return fromUrl;
  const stored = localStorage.getItem("gujia-language");
  return stored === "en" || stored === "zh" ? stored : "zh";
}

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.zh;
  document.documentElement.dataset.lang = lang;
  document.documentElement.lang = lang === "zh" ? "zh-Hans" : "en";

  const path = window.location.pathname;
  document.title = path.includes("/about/") ? dictionary.titleAbout : dictionary.titleHome;

  document.querySelectorAll(".brand-text").forEach((brand) => {
    const lines = brand.querySelectorAll("span");
    if (lines[0]) lines[0].textContent = lang === "zh" ? "顾家" : "GUJIA";
    if (lines[1]) lines[1].textContent = lang === "zh" ? "GUJIA" : "CARE";
  });

  document.querySelectorAll('[data-lang-option="zh"]').forEach((node) => {
    node.textContent = "中文";
  });
  document.querySelectorAll('[data-lang-option="en"]').forEach((node) => {
    node.textContent = "English";
  });

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) node.textContent = dictionary[key];
  });

  localStorage.setItem("gujia-language", lang);
}

if (menuButton && navLinks) {
  menuButton.addEventListener("click", () => {
    const isOpen = navLinks.classList.toggle("is-open");
    menuButton.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.addEventListener("click", (event) => {
    if (event.target instanceof HTMLAnchorElement) {
      navLinks.classList.remove("is-open");
      menuButton.setAttribute("aria-expanded", "false");
    }
  });
}

if (languageButton) {
  languageButton.addEventListener("click", () => {
    const next = document.documentElement.dataset.lang === "zh" ? "en" : "zh";
    applyLanguage(next);
  });
}

applyLanguage(getRequestedLanguage());
